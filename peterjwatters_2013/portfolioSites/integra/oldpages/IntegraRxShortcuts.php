<b>Alt & N</b> – Next item<br>
<b>Alt & D </b>– Set item as Not Dispensed<br>
<b>Alt & F </b>– Set item as On-Loan<br>
<b>Alt & + </b>– Set item as Phased<br>
<b>Alt & - </b>– Next item<br>
<b>Ctrl & letter </b>– Select control with ~<br> 
	 

