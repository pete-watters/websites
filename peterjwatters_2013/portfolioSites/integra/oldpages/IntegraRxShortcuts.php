<h3>PMR Shortcuts:</h3>
<img src="images/TopKybdStrip2.jpg" /><br/>
Key:	Function:	Comment:	 <br>  <br>
<b>F1</b>	Help	To open the Help file	 <br>  
<b>F2</b>	Patient Search	To open a patient file<br>	   
<b>F3</b>	Patient Maintenance	To Edit Patient details	<br>   
<b>F4</b>	Ad-Hoc Label	To Product an ad-hoc label	   <br>
<b>F5</b>	DPS Analysis	To view a patients DPS co-payment values	   <br>
<b>F6</b>	Quick Sale	Insert an account sale.  (Standalone version only).	   <br>
<b>F7</b>	Quick Order	Opens the Quick Order form screen	   <br>
<b>F8</b>	Double click / Repeat	Repeat the selected drugs / script – invoke double left click<br>	   
<b>F9</b>	Cancel	Invokes the Cancel button on any screen	   <br>
<b>F10</b>	Open Modules menu	Use F10 and the number or letter of the module you wish to open.	 <br>  
<b>F11</b>	Price Lookup	To Calculate the price of a dispense	   <br>
<b>F12</b>	Ok / Complete	Invokes the Ok or Complete button on any screen<br>	 
 

