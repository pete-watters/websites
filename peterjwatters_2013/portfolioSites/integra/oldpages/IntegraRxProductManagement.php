<li>	The Stock Adjustments report allows practice owners and managers to monitor general stock shrinkage and pilferage</li>
<li>	Having floor staff run a Stock Adjustment report on their respective sections on a regular basis, ensures that Practice SOP’s are been followed and that each section of the shop is been managed.</li>
