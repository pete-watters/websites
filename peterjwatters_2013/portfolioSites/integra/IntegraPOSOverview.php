<h3> <font color="#046b95">Integra POS - Overview </font></h3>
<p>
Complementing Integra RX, but also a standalone till solution in its own right, IntegraPOS is the first EPoS package fully integrated with the Dispensary and therefore aimed specifically at improving the workflow of Irish Pharmacies.</p>
<p>
It is the most efficient and rapidly implemented EPoS system available to Irish Pharmacies today, being able to show patients details and accounts, process DPS payments, managing stock in the dispensary and at the front of shop, allowing orders to be sent and deliveries processed from anywhere in the pharmacy, and much more…
</p>
<p>
<ul>
<li>Train new users in 10 minutes </li>
<li>Unique Prescription Cash Management module</li>
<li>View Patient's PMR at the Till point</li>
