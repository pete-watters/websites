<h3> <font color="#046b95">Integra Script Scan</font></h3>

<p>
Integra Script Scan allows pharmacist to collect a more complete patient record, save time and space.
</p>
<p>
With an Integra Script Scan Link installed, a pharmacist can now Scan a private prescription when it is presented and save it directly to the Patients record.  This means no fiddling around with a fax machine or photocopier to make a copy and then wasting time then or later that night filing it.
</p>
<p>
Just dispense the script as normal, Scan, Stamp and Go.
</p>
<p>
There are also advantages for GM & GR scripts.  When a patient presents their 3 month GR script or 3 single GM scripts, you could dispense the script as normal, Scan the Top copy and file the scripts.  When the patient presents for their next dispense, and you wish to consult the prescription, simply open the patients file, as you normally would – then click the View Scan button to view the stored image of the script.
</p>
<p>
The Integra Script Scan Link won’t make Irish Pharmacy paperless anytime soon, but it’s a step in the right direction.
</p>